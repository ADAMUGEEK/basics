# Describes all the operators related to arithmatic

# Different operators are available for 
# + , - , * , / , %

# Addition Operator
add = 1 + 2
print('add:', add)

# Negative Operator
sub = 2 - 1
print('sub:', sub)

# Multiplication Operator
mul = 2 * 1
print('mul:', mul)

# Division Operator
div = 2/1
print('div:', div)

# Modulus Operator
mod = 2%2
print('mod:', mod)

# Precedence of operators
# / , * , - , +


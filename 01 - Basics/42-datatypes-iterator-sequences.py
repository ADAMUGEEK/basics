#       - different sequence data types lists and tuples (immutable lists) are created
#       - list and tuple functions used to create a list and tuple respectively
# 
# RESOURCES
# 


# # # LIST
# Integer based list
var = [1, 2, 3]

# List function to create a list
var = list(1, 2)

# String Type List (can also be any other type based list)
var = ["s", "t", "r", "i", "n", "g"]

# Mixed Type List
var = [1, 1.2, "s", True, None, False, [1, "s"], (1, "s"), {"s": 20}]


# # # TUPLE
# Integer Type Tuple (can also be any other type based Tuple)
var = (1,)

# Tuple function to create a tuple
var = tuple(1,2)

# Integer Type Tuple (can also be any other type based Tuple)
var = (1, 2)

# String Type Tuple (can also be any other type based Tuple)
var = ("s", "t", "r", "i", "n", "g")

# Mixed Type Tuple
var = (1, "s")

# Mixed Type Tuple
var = (1, 1.2, "s", True, None, False, [1, "s"], (1, "s"), {"s": 20})

# String Type - behaves as Tuple iterator
var = "stringtypes"

# # # DICTIONARY
# Dictionaries are key value pairs with key as a reference for item value
# Dictionaries can have integer or string as key
# Key is unique
# Value can be of any type

# Dictionary can have integer as key
var = {1: "s"}

# Dictionary can have string as key
var = {"name": []}

# Using dict function to create a dictionary
var = dict({"name": [1, 2]})

# Using dict function to create a dictionary
var = dict({1: [1, 2]})

# Dictionary can have string or integer as key
var = dict({"s": [1, 2], 1: 2})

foo = ['hello', 'hi', 'howdy', 'heyas']

# Object types may have methods for self modification or some operation
# example usage for .index() that returns the index of the item provided
# returns the occurance
print(foo.index('hello')) 

foomer = ['hello', 'hi', 'howdy', 'heyas', 'hello']
# returns the index of first occurance
print(foomer.index('hello')) 

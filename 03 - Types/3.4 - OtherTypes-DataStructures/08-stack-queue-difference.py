# Stack and Queue both are the non-primitive abstract data structures

# The main differences between stack and queue are that STACK uses LIFO (last in first out) method to access and add data elements whereas 
#       QUEUE uses FIFO (First in first out) method to access and add data elements

# Stack has only one end open for pushing and popping the data elements on the other hand 
#       Queue has both ends open for enqueuing and dequeuing the data elements 
#       (commonly the first end where items were added first)

# STACK is visualized as a VERTICAL structure and QUEUE a HORIZONTAL structure

# STACK does not have variants.	
# QUEUE has variants like circular queue, priority queue, doubly ended queue.

# STACK
# Insert operation is called push operation
# Delete operation is called pop operation
# QUEUE
# Insert operation is called enqueue operation
# Delete operation is called dequeue operation

# STACKS maintain only one pointer to access the list, called the top, 
#         which always points to the last element present in the list. 	
# QUEUE maintain two pointers to access the list. The front pointer always 
#         points to the first element inserted in the list and is still present, 
#         and the rear pointer always points to the last inserted element.

# STACK require only one pointer so-called “top” pointer.
# QUEUE uses two pointers so-called “Front” and “Rear” pointers.

# Collection of dinner plates at a wedding reception is an example of STACK.	
# People standing in a file to board a bus is an example of QUEUE.

# STACK: Operations includes testing null stack, finding the top element in the stack, 
#         removal of top most element and adding elements on the top of the stack.
# QUEUE: Operations include testing null queue, finding the next element, removal of 
#         elements and inserting the elements from the queue. 

